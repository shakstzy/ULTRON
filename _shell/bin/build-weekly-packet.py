#!/usr/bin/env python3
"""
build-weekly-packet.py — compose a markdown packet at
`_shell/audit/packets/<YYYY>-W<WW>.md` from the prior week's
- _shell/audit/promotion-candidates.md
- _shell/audit/alias-candidates.md (when populated by audit)
- _graphify/super/GRAPH_REPORT.md (when populated)
- workspaces/<ws>/_meta/log.md tail (last 7 days)
- workspaces/<ws>/_meta/schema-proposals.md, learning-proposals.md (open items)
- _logs/*.err.log (last 7 days, non-empty only)

Skeleton: real synthesis (prose summary) is added by the weekly-review stage's
Claude pass; this script produces the structured frame for that pass to fill.

Usage:
    build-weekly-packet.py [--out <path>]
"""
from __future__ import annotations

import argparse
import os
import sys
from datetime import datetime, timedelta, timezone
from pathlib import Path

ULTRON_ROOT = Path(os.environ.get("ULTRON_ROOT", str(Path.home() / "ULTRON")))


def iso_week_label(now: datetime | None = None) -> str:
    now = now or datetime.now(timezone.utc)
    yyyy, ww, _ = now.isocalendar()
    return f"{yyyy}-W{ww:02d}"


def read_or_skip(path: Path, label: str) -> str:
    if not path.exists():
        return f"### {label}\n\n_(no file at `{path.relative_to(ULTRON_ROOT)}`)_\n"
    text = path.read_text(errors="ignore").strip()
    if not text:
        return f"### {label}\n\n_(empty)_\n"
    return f"### {label}\n\nFrom `{path.relative_to(ULTRON_ROOT)}`:\n\n{text}\n"


def tail_log(path: Path, days: int = 7) -> str:
    if not path.exists():
        return "_(no log)_"
    cutoff = datetime.now(timezone.utc) - timedelta(days=days)
    out_lines: list[str] = []
    for raw in path.read_text(errors="ignore").splitlines()[-2000:]:
        ts_str = raw.split(" |", 1)[0].split(" ", 1)[0] if " |" in raw else raw[:10]
        try:
            ts = datetime.fromisoformat(ts_str.replace("Z", "+00:00"))
        except (ValueError, IndexError):
            continue
        if ts >= cutoff:
            out_lines.append(raw)
    return "\n".join(out_lines) if out_lines else "_(no entries in window)_"


def workspace_section(ws_dir: Path) -> str:
    name = ws_dir.name
    log_tail = tail_log(ws_dir / "_meta" / "log.md")
    schema_p = (ws_dir / "_meta" / "schema-proposals.md").read_text(errors="ignore") if (ws_dir / "_meta" / "schema-proposals.md").exists() else "_(none)_"
    learn_p = (ws_dir / "_meta" / "learning-proposals.md").read_text(errors="ignore") if (ws_dir / "_meta" / "learning-proposals.md").exists() else "_(none)_"
    return (
        f"### Workspace: {name}\n\n"
        f"#### Recent log entries\n\n```\n{log_tail}\n```\n\n"
        f"#### Schema proposals\n\n{schema_p}\n\n"
        f"#### Learning proposals\n\n{learn_p}\n"
    )


def collect_recent_failures(days: int = 7) -> str:
    logs_dir = ULTRON_ROOT / "_logs"
    if not logs_dir.exists():
        return "_(no _logs/ directory)_"
    cutoff = (datetime.now(timezone.utc) - timedelta(days=days)).timestamp()
    entries: list[str] = []
    for p in sorted(logs_dir.glob("*.err.log")):
        try:
            stat = p.stat()
        except OSError:
            continue
        if stat.st_mtime < cutoff or stat.st_size == 0:
            continue
        # Tail last ~50 lines.
        body = p.read_text(errors="ignore").splitlines()[-50:]
        body_text = "\n".join(body) if body else "_(empty)_"
        entries.append(f"- `{p.relative_to(ULTRON_ROOT)}`:\n\n```\n{body_text}\n```")
    return "\n\n".join(entries) if entries else "_(no failures in window)_"


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--out")
    args = ap.parse_args()

    label = iso_week_label()
    out_path = Path(args.out) if args.out else (ULTRON_ROOT / "_shell" / "audit" / "packets" / f"{label}.md")
    out_path.parent.mkdir(parents=True, exist_ok=True)

    sections: list[str] = []
    sections.append(f"# Weekly Review Packet — {label}\n")
    sections.append(
        f"_Generated by `_shell/bin/build-weekly-packet.py` at "
        f"{datetime.now(timezone.utc).isoformat()}._\n"
    )

    sections.append("## 1. Promotion candidates\n")
    sections.append(read_or_skip(ULTRON_ROOT / "_shell" / "audit" / "promotion-candidates.md", "Cross-workspace slug candidates"))

    sections.append("## 2. Alias candidates\n")
    sections.append(read_or_skip(ULTRON_ROOT / "_shell" / "audit" / "alias-candidates.md", "Alias candidates"))

    sections.append("## 3. Graphify super-graph report\n")
    sections.append(read_or_skip(ULTRON_ROOT / "_graphify" / "super" / "GRAPH_REPORT.md", "Super-graph report"))

    sections.append("## 4. Per-workspace activity (last 7 days)\n")
    workspaces_dir = ULTRON_ROOT / "workspaces"
    if workspaces_dir.exists():
        for ws_dir in sorted(workspaces_dir.iterdir()):
            if not ws_dir.is_dir() or ws_dir.name.startswith("_"):
                continue
            sections.append(workspace_section(ws_dir))

    sections.append("## 5. Job failures (last 7 days)\n")
    sections.append(collect_recent_failures(days=7))

    sections.append("\n## 6. Synthesis\n\n_(this section is filled by the weekly-review stage's Claude pass)_\n")

    out_path.write_text("\n\n".join(sections))
    print(f"wrote {out_path.relative_to(ULTRON_ROOT)}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
